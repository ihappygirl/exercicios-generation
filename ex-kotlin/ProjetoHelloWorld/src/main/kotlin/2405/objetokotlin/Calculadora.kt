package `2405`.objetokotlin

class Calculadora {

    companion object {
        var n1 = 0.0
        var n2 = 0.0

        fun soma(): Double {
            return n1 + n2
        }

        fun sub(): Double {
            return n1 - n2
        }

        fun multi(): Double {
            return n1 * n2
        }
    }

}